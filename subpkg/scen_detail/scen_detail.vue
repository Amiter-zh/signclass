<template>
	<view class="ho">
		<swiper :autoplay="true" :interval="3000" :duration="1000" :circular="true">
			<swiper-item v-for="(item,i) in scenInfo.swiper" :key="i">
				<image :src="item">
			</swiper-item>
		</swiper>
		<view class="main">
			<view class="header shadow-project">
				<view class="line1">
					{{scenInfo.title}}
				</view>
				<view class="line2" v-if="scenInfo.time">
					<text class="icon-time margin-right-xxs">开放时间：</text>{{scenInfo.time}} 全天
				</view>
				<view class="line3" v-if="scenInfo.address">
					<text class="icon-location margin-right-xxs">地址：</text>{{scenInfo.address}}
				</view>
			</view>
			<view class="content shadow-project">
				<view class="item" v-if="scenInfo.ticket">
					<view class="title">门票攻略</view>
					<view class="desc"><text user-select>{{scenInfo.ticketinfo}}</text>
					</view>
				</view>
				<view class="item">
					<view class="title">简介</view>
					<view class="desc"><text user-select>{{scenInfo.desc}}</text>
					</view>
				</view>
				<view class="item" v-if="scenInfo.traffic">
					<view class="title">交通攻略</view>
					<view class="desc"><text user-select>{{scenInfo.traffic}}</text>
					</view>
				</view>
				<view class="item" v-if="scenInfo.other">
					<view class="title">其他信息</view>
					<view class="desc"><text user-select>{{scenInfo.other}}</text>
					</view>
				</view>
			</view>
		</view>

		<view class="goods_nav">
			<uni-goods-nav :fill="true" :options="options" :buttonGroup="buttonGroup" @click="onClick"
				@buttonClick="buttonClick" />
		</view>

	</view>
</template>

<script>
	let id;
	import {
		mapMutations,
		mapState
	} from 'vuex'
	export default {
		data() {
			return {
				SwiperList: [],
				scenInfo: {},
				options: [{
					icon: 'star',
					text: '收藏',
					infoBackgroundColor: '#007aff',
					infoColor: "red"
				}, {
					icon: 'cart',
					text: '订单',
				}],
				buttonGroup: [{
						text: '加入收藏',
						backgroundColor: '#23ccff',
						color: '#fff'
					},
					{
						text: '立即购买',
						backgroundColor: '#18c70e',
						color: '#fff'
					}
				]

			};

		},
		computed: {
			...mapState('m_user', ['token']),
		},
		onLoad(e) {
			id = e;
			this.getDate();
			this.getInfo();
			this.updatecount();
		},
		methods: {
			...mapMutations('m_cart', ['addTocart']),
			onClick(e) {
				console.log(e);
				if (e.content.text === '收藏') {
					uni.switchTab({
						url: '/pages/star/star'
					})
				}
				if (e.content.text === '订单') {
					uni.navigateTo({
						url: '/subpkg/cart/cart'
					})
				}
			},
			buttonClick(e) {
				console.log(e);
				if (e.content.text === '加入收藏') {
					const scens = {
						scen_id: this.scenInfo._id, // 商品的Id
						scen_title: this.scenInfo.title, // 商品的名称
						scen_time: this.scenInfo.time, // 商品的价格
						scen_small_logo: this.scenInfo.swiper[0], // 商品的图片
						scen_address: this.scenInfo.address
					}
					this.addTocart(scens)
					uni.showToast({
						title: "添加成功"
					})
				}
				if (e.content.text === '立即购买') {
					if (!this.scenInfo.ticket) {
						uni.showToast({
							icon: 'error',
							title: "该景点无需门票"
						})
					} else if (!this.token) {
						uni.showToast({
							title: '请登录后购买',
							icon: 'error'
						})
					} else {
						uniCloud.callFunction({
							name: 'buy',
							data: {
								scen_id: this.scenInfo._id,
								scen_title: this.scenInfo.title,
								scen_ticket: this.scenInfo.ticket,
								scen_small_logo: this.scenInfo.swiper[0],
							}
						}).then(res => {
							console.log(res);
							uni.showToast({
								title: "购买成功！"
							})
						})
					}
				}
			},
			updatecount() {
				uniCloud.callFunction({
					name: 'upc',
					data: {
						id
					}
				}).then(res => {

				})
			},
			getInfo() {
				uniCloud.callFunction({
					name: "scen_detail",
					data: {
						id
					}
				}).then(res => {
					console.log(id);
					this.scenInfo = res.result.data[0]
					console.log(this.scenInfo);
					uni.setNavigationBarTitle({
						title: this.scenInfo.title
					})
				}).catch(() => {
					uni.showToast({
						icon: 'error',
						title: "发现错误！！！"
					})

				})
			},
			getDate() {
				uniCloud.callFunction({
					name: "rec_get"
				}).then(res => {
					this.SwiperList = res.result.data
				}).catch(() => {
					uni.showToast({
						icon: 'error',
						title: "发现错误！！！"
					})

				})
			},
		}
	}
</script>

